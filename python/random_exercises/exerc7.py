# Receba os valores do comprimento, largura e altura de um paralelepípedo. 
# Calcule e mostre seu volume

c = float(input('Informe o valor do comprimento do paralelepípedo: '))
l = float(input('Informe o valor da largura do paralelepípedo: '))
a = float(input('Informe o valor da altura do paralelepípedo: '))

v = c*l*a
print('O volume do paralelepípedo é: ', v)