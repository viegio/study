# Receba o ano de nascimento e o ano atual. 
# Calcule e mostre a sua idade e quantos anos terá daqui a 17 anos

anoa = int(input('Informe o ano atual: '))
anon = int(input('Informe o ano de nascimento: '))

ia = anoa - anon
print('A idade atual é: '+str(ia))
ifu = ia + 17
print('A idade futura será: '+str(ifu))